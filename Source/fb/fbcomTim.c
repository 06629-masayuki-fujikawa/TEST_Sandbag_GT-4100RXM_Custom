{																				/*								*/
	FBcom_2msT1 = 0x8000;														/*								*/
}																				/*								*/
																				/*								*/
/*[]----------------------------------------------------------------------------------------------------------[]*/
/*|             FBcom_2mTimeout()																			   |*/
/*[]----------------------------------------------------------------------------------------------------------[]*/
/*|	return : 1=timeout, 0=didn't timeout (or didn't request timer)											   |*/
/*[]----------------------------------------------------------------------------------------------------------[]*/
/*| Author		: S.Takahashi(FSI)																			   |*/
/*| Date		: 2011-10-04																				   |*/
/*| Update      :																							   |*/
/*[]------------------------------------------------------------------ Copyright(C) 2005 AMANO Corp.----------[]*/
uchar	FBcom_2mTimeout( void )													/*								*/
{																				/*								*/
	if( 0 == FBcom_2msT1 ){														/*								*/
		return	(uchar)1;														/*								*/
	}																			/*								*/
	return	(uchar)0;															/*								*/
}																				/*								*/

